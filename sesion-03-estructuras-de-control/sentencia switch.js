// Sentencia Switch

let nota = 1

switch(nota){
    case 5 :
        console.log ("enhorabuena , has obtenido un sobresaliente");
        break;
    case 4:
        console.log ("buen trabajo, pero podrias hacerlo mejor");
        break;
    case 3:
        console.log ("has obtenido un suficiente");
        break;
    case 2:
        console.log ("no has aprovado por poco");
        Break;
    
    case 1:
        console.log ("no has estudiado nada, trabaja");
    break;
    default:
        console.log("error");
        Break;
}